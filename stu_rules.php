							<h2>Rules & Regulations</h2>

					<div id="rules">
<p align="center"><strong><span style="text-decoration: underline;">PLACEMENT POLICY FOR PLACEMENT SESSION 2011-12</span></strong></p>
<p>The placement policy will be applicable to all students registered for the 2011-12 campus placements of IIT Kanpur and is to be followed during the entire duration of the placement season.</p>
<p><strong><span style="text-decoration: underline;">Eligibility Criteria</span></strong><span style="text-decoration: underline;"><strong>for B.Tech, Dual Degree, M. Tech, MSc. And PhDs</strong></span></p>
<ul><li> Student of IIT Kanpur expected to complete his/her degree by July 2012 is eligible to register.</li><li>
Sponsored students, i.e. students under QIP or those who have signed any bond for pursuing studies at the IIT Kanpur must produce NO OBJECTION CERTIFICATE from the concerned authorities/ agencies prior to registering with SPO.</li><li>

Students can register for placements only once.</li></ul>
<p><strong><span style="text-decoration: underline;">Guidelines for Policy</span></strong></p>
<ul><li>The role of the SPO (Students' Placement Office) is of a<strong> facilitator and counselor </strong>for placement related activities. SPO does <strong>NOT guarantee </strong>a job. Applying for a company or joining a company is entirely the responsibility/decision of the student concerned.</li>
<li>The placement facility is      available to all the students registered with SPO through<strong></strong>the      policy<strong> ONE JOB TO ONE STUDENT AT THE FIRST INSTANCE</strong></li>
<li>There      is<strong> no restriction </strong>on applying/appearing in      interviews until the first job is secured. A student will be considered to      have secured a job if his/her name appears in the selection list or an      appointment letter is received in the SPO.</li>

<li>Students      must keep their<strong> Identity Card </strong><strong></strong>with them at the time of      PPT/Test/Group Discussion/ Interviews, and produce the same when demanded      by the visiting team or SPO staff or their representatives.</li>
<li>Once      a student has been shortlisted by a company for placement process he /she      is <strong>NOT allowed to contact </strong>the members of the team visiting      the campus forplacement purpose without prior permission of      the Chairman SPC.</li>
<li>The SPO strongly<strong> discourages the students to go for off campus placements </strong>in the companies that already come to IITK campus as this affects the company-institute relations adversely. Students are also requested to forward contacts they have, if any, in different companies so that SPO may formally invite these companies for placements.</li>
<li>If a student is found doctoring      his/her resume submitted at SPO his/her registration at SPO will be      cancelled immediately and also the matter will be referred to SSAC.</li>
</ul>
<p><strong><span style="text-decoration: underline;">Pre Placement offers (PPOs)</span></strong></p>

<ul>
<li>All Pre      Placement Offers by any company whatsoever, extended to any student, have      to be immediately intimated to the Placement Office by the student after      registration. This also includes offers extended by companies that are not      participating in the 2011-12 campus placements.</li>
<li>All companies      contacted will also be asked in advance about any Pre Placement offers      that have been extended by them and the company will be informed about our      PPO policy in advance.</li>
<li>Also, any form      of intimation from a company about the possibility of a PPO being granted      in future should be informed to the Placement Office. The Placement Office      will take up the matter with the company further and the student will be      allowed to sign Job Announcement Form(JAF) as per the normal policy until a decision      regarding the grant of a PPO is made.</li>
<li>A student offered a Pre Placement Offer through SPO must accept/reject the offer prior to the date specified by the company.</li>
<li>Students accepting or denying a PPO will have to immediately notify the Placement Office of the same. If the PPO is accepted, the job will be recorded and he/she will NOT be allowed to participate in the campus placements. However, he/she will be allowed to participate in the process if the offer is rejected. The rejection letter will be sent to the company through SPO.</li></ul>
<p>Any student found to have breached any of these rules or secured a PPO and not informed the placement office of the same will be debarred from the placement process. If at the time of breach the student is already placed, the company in which he/she is placed will be intimated of the violation.</p>

<p><strong><span style="text-decoration: underline;">Penalty Structure</span></strong></p>
<p>Each student will be assigned a total of 10 points at the time of registration with SPO. Points will be deducted from his record for misconduct as per the following rules. Losing all the 10 points or more will lead to deregistration from SPO. Re-registration may be done at the discretion of Chairman, SPC and/or after paying a monetary penalty.</p>

<ul>
<li>A student will be allowed to be      exempted from compulsory attendance for PPT for a maximum of 2 companies      due to genuine      reasons* by filling a leave application form which can be      downloaded from (<a href="./site_docs/PPT_Leave_Application.doc">PPT Leave Application</a>)      within 2 days after PPT.</li>

<li>Student will not be allowed to participate in the selection process of the company for which he/she has missed the PPT (Exempting the 2 companies missed due to genuine reasons). However, he/she may be allowed to participate in the process of that particular company after deduction of 4 points.</li>
<li>Opting out of      the selection process of a company at any stage after applying may lead to      deduction of minimum of 5 points and maximum of 10 points. For ex- 6      points for missing test, 7 for missing GD, 10 for missing interview.</li>
<li>Any sort of misbehaviour on the part of students which affects the decorum of the PPT or the selection process or the reputation of the SPO attracts a minimum penalty of 2 points. The fine may be increased at the discretion of the Placement Office as per the severity of the situation. eg- Asking inappropriate questions during PPT or Interview.</li>
<li>Cheating in all      forms or marking proxies during the selection process is strictly      prohibited and those caught indulging in such activities would lose all      their points and debarred from the placement process. Also note that no      concessions in any form shall be provided in such case.</li>
</ul>
<p>*Genuine reasons include health problems or other reasons subject to approval of Chairman, SPC. PPT missed due to academic clashes will not be counted among the 2 companies. Attendance will be marked on submitting the PPT leave application form.</p>

<p><strong><span style="text-decoration: underline;">Guidelines for Master Resume:</span></strong></p>
<p>Important guidelines related to master resume:</p>
<ul>
<li>Master resume will NOT be sent to      the company, you need to apply with a different resumefor      every company</li>
<li>No bar on the length of master      resume, but each and every point will need verification</li>
<li>The resume with which one applies      for a particular company must be a subset of the master resume</li>
<li>One cannot add/change any      point/sub point that is written in the master resume while making his/her      resume
<ol style="list-style:lower-alpha" >
<li>One is allowed to delete       points/sub points</li>
<li>If you need to state your point       in a different way for the technical and non-technical profile, we       suggest you to include it in the master resume itself even if it causes       repetition</li>

</ol></li>
<li>Any change in the master resume      MUST be done with the permission of SPO; writing the same point in a      different way will be considered as a 'change'</li>
<li>Your resume, before being sent to      the company, will be verified by the software and if found different from      the master resume even by 5% then your application will get withdrawn from      that company and will also attract penalty on the part of student</li>
</ol>
