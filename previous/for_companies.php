				<div class="article" style="border:none;" align="justify">
							<h2>FAQ's - For Companies</h2>
				</div>
				<ul>
				<li><strong>What are the different ways a company can hire students from the campus?</strong><br/>
					The different ways of recruiting students are:
					<ul>
					<li>Campus Recruitment Program in which final year students participate</li>
					<li>Summer Internship Program for pre final year students which can be converted into a pre placement offer</li>
					</ul>
				</li>
				<br/>
				<li><strong>How can a company register itself for the Campus Recruitment Program?</strong></br>
					<p>To participate in the Campus Recruitment Program, a company must first contact the 
					placement office or register on the website. The company is then sent the login details which would let 
					them login in our Placement Automation System (PAS) enabling them to post job opportunities &
					see the profiles of students interested in their job opening.</p>
					<p><a href="./register.php"><strong>Register here</strong></a></p>
					<p> You can also find the detailed Placement Procedure <a href="./placement_proce.php">here</a></p>
				</li>
				<li><strong>Is there any fee associated with the registration?</strong></br>
					<p>There is no fees associated with the registration or the Placement Process</p>
				</li>
				<li><strong>When does the recruitment program start?</strong></br>
					<p>The recruitment season is held in December every year and the PPT's can be held anytime between 10th of August to mid-November.
					For a detailed itinerary, Please refer to the <a href="./site_docs/Calendar.pdf">Employer Calendar</a></p>
				</li>
				<li><strong>What is the procedure to schedule a pre-placement talk (PPT)?</strong></br>
					<p>A company will only be allowed to hold its PPT after filling up the job announcement form. 
					A typical pre placement session is scheduled for 60 minutes. In view of the academic calendar of 
					the students, the session are scheduled between 6pm to 9pm on weekdays, and between 10am and 5pm 
					hours on Saturdays and Sundays. Student volunteers will get in touch with the company to fix a 
					mutual convenient time for the session.</p>
				</li>
				<li><strong>What kind of information do the students expect in PPTs?</strong></br>
					<p>The pre-placement talk is a platform for interaction between the company and the students. The presentation is 
					followed by a Q&A session where students interact with company officials to get a better understanding of the 
					company and potential roles. The important insights that the students expect from the PPT are:
					<ul>
						<li>Profile and reputation of the corporation</li>
						<li>Career roles and responsibilities offered in different types of profiles</li>
						<li>Locations where the student is likely to be posted</li>
						<li>Compensation packages</li>
					</ul>
					</p>
				</li>
				<li><strong>On what basis is the slot allotted to a company for the final placement?</strong></br>
					Slotting is done subject to the following parameters:
					<ul>
						<li>Student Preferences</li>
						<li>Work profile</li>
						<li>Compensation package</li>
						<li>Career Prospects</li>
						<li>Student Intake</li>
						<li>Past relationship with IIT Kanpur</li>
					</ul>
				</li></br>
				<li><strong>What are the infrastructural facilities available on the campus for the placement process?</strong></br>
					The campus is equipped with state of the art communication, computing, and presentation facilities to facilitate a smooth placement process:
						<ul>
						<li>Teleconferencing and video conferencing facility</li>
						<li>Linux and windows lab for online tests</li>
						<li>Centrally air conditioned auditorium, lecture halls, seminar and conference rooms for presentations, group discussions and interviews</li>
						</ul>
				</li><br/>
				<li><strong>Are there multiple companies recruiting in the same slot? How is the case of multiple offers resolved?</strong><br/>
					<p>Yes there are multiple companies recruiting in the same slot.The results of all the companies are announced at the end of the slot and 
					if a student gets multiple offers then he is allowed to choose between one of them and inform the office of his decision before the 
					beginning of the next slot.</p>
				</li>
				<li><strong>Can a company interview the students already placed in other company?</strong><br/>
					<p>IIT Kanpur follows a “One student; One job” policy so once the job is registered against the student, he/she is not entitled to sit 
					for any other company.</p>
				</li>
				<li><strong>Is there any facility for the accommodation available on the campus?</strong><br/>
					<p>On campus accommodation on payment basis in visitors' guest house is available subject to availability. For alternate arrangements, 
					the list of hotels is available <a href="./site_docs/Hotels.pdf">here</a></p>
				</li>
				<li><strong>How can one commute between the campus & airport/railway station?</strong><br/>
