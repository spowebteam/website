<p align="center"><strong><span style="text-decoration: underline;">INTERNSHIP POLICY FOR PLACEMENT SESSION 2010-11</span></strong></p>
<p>The internship policy will be applicable to all the students who will register for the 2010-11 internship session of IIT Kanpur through SPO and is to be followed during the entire duration of the season.&nbsp;</p>
<p><strong><span style="text-decoration: underline;">Eligibility Criteria</span></strong></p>
<ul><li>Students of 3rd year(UG) as well as pre-final year of<strong> </strong>M.Sc. INT. courses <strong>only can</strong> apply for internships through SPO.</li><li>
Student can register only once.</p></li></ul>

<p><strong><span style="text-decoration: underline;">Rules And Regulations: </span></strong></p>
 <ul>
 <li> One Student, One Internship for the first instance.</li>
 <li> A student must attend the PPT of the company if he has applied for its internship process.</li>
 <li> If a student withdraws from a company's process after applying for its process, he will be deregistered from SPO and will not be able to apply for any Internship through SPO.</li>
 <li> If a student leaves the process without prior information and genuine reason,* then he will be deregistered from SPO and will not be able to apply for any Internships through SPO.</li>

 <li> If a student gets an Internship from anywhere other than SPO, he should inform SPO as soon as possible so that all his applications for internship could be withdrawn.</li>
 <li> If a student left the Intern of SPO, then he will be subjected to penalty at the time of final placements. Penalty<strong> </strong>is that he will be allowed to sit in only two companies in the first week (7 days) of placement season.</li>
 <li> If a student directly contacts a Company that comes through SPO, then it would be considered as an Off-Campus attempt.</li>
 <li> If a student gets more than one offer in a single day then he has the option of choosing between them at the end of the day. The choice should be communicated to 
 SPO after the decision has been made on that day itself</li>
 </ul>
 <br>
<p>*Genuine reasons include health problems, academic clashes or other reasons subject to approval of Chairman, SPC</p>
