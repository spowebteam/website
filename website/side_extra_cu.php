							<h2>Extra Curriculars</h2>
					
					<p>IIT Kanpur  provides an environment that nurtures leaders in addition to sound engineers.  Students independently manage several club activities, national level festivals,  administration of hostels and thus emerge as winner not only in national level  competitions but also in their corporate life as well. This provides a platform  to the students to hone their inter-personal skills and provides a holistic  education.</p>
<p><b><h3><u>Festivals</b></u></h3><br />

  The National  Festivals of IIT Kanpur budget around 1.5 crore provide a unique opportunity  where the students can experience management lessons first hand. Right from the  stage of conceptualization to final implementation the students manage  everything on their own. The organizing team gets to interact and deal with  corporate, celebrities and hence inculcate managerial and inter-personal skills.</p>
<p><b>Techkriti:</b> The Global  techno-entrepreneurial festival of IIT Kanpur attracts the best talent from all  across the world in its competitions. Several high profile dignitaries from the  field of science and technology also attend the festival.</p>
<p><b>Antaragni</b>: The festival  in its 43rd year has evolved as the biggest cultural festival in North  India which witnesses more than 6000 participants. Some of the best known  artists in the country from the field of music, dance, theatre and arts have  been a part of the festival in the past. A team of more than 600 students works  for around nine months to put up the show.</p>
<p><b>Udghosh: </b>The  national level sports festival of IIT Kanpur<b> </b>sees participation from all corners of the country. The festival provides a platform for the  participating teams and the organizers to inculcate a sense of team work and  leadership.</p>
<p><b><h3><u>Inter-Hostel Competitions</u></h3></b><br />

  The inter hall  competitions see the best of friends turn into the fiercest of competitors and  hence bring out the best of talents in the students. </p>
<p><b>Galaxy:</b>  The  cultural championship.<br />
  <b>Varchasva: </b> The  sports championship.<br />
  <b>Takneek: </b> The science and technology  championship. <br />

  <b>Impressions: </b> The cultural event for post graduate students.</p>
<p><b><h3><u>Club Activities</u></h3></b><br />
  Club activates  are managed by the students under the aegis of Students&rsquo; Gymkhana. Club  activities provide the learning outside the classroom which sensitizes students  to other aspects of life. Clubs organize several workshops and competitions all  around the year in addition to representing IIT Kanpur at national level  competitions. </p>
<p><b>Science  and Technology Clubs</b><br />
  The  Science and Technology Council has  several clubs like the Astronomy Club, Business club, Electronics club,  Programming club, Gliding club, Ham club,  Aeromodelling Club, Photography club, Robotics Club.</p>

<p><b>Cultural  Council</b><br />
  Cultural  Council provides platform to every cultural taste through the Dance Club,  Dramatics Club, Fine Arts Club, Music Club, Quiz Club, English Literary  Society, Hindi Sahitya Sabha, Vivekananda Samiti and SPIC MACAY Society.</p>
<p><b>Films  and Media Council:</b><br />
  The FMC Council  manages Vox Populi-the students&rsquo; newspaper of IIT Kanpur, Meander - The  Student&rsquo;s Magazine, Animation club, Student&rsquo;s Television Society.</p>
<p><b>Games  and Sports council</b>:<br />

  In addition the  several sports and games teams the institute also has Adventure club,  Tae-Kwon-Do Club, Nature club, Bridge Club and Chess club</p>
<p><b>Presidential  Council:</b><br />
  Book Club, GLDC,  Prayas (Student run social group)</p>
<p><b>E- Cell: </b>An Entrepreneurship Cell has been established and our  annual Science and Technological festival (Techkriti) has been transformed to a  Technology and Entrepreneurship festival which includes a number of Business  idea games and events.</p>

					
